public class Alphabet
 {
    public static void main(String[] args) 
    {
        char c = '3';
         if( (c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
          {
               System.out.println( " is an alphabet:"+c);
          }
          else
          {
            System.out.println(" is not an alphabet:"+c);
          }
    }
}

