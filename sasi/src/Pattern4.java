public class Pattern4
 {
    public static void main(String[] args)
     {
        int line=10;
        int j,i=1;
        for(i=1;i<=line;i++)
        {
            for(j=1;j<=i;j++)
            {
                System.out.print(i+j+" ");
            }
            System.out.println();
        }
    }
}
