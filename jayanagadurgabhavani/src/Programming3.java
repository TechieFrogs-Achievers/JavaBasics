public class Programming3 
{
    public static void main(String[] args) 
    { 
  
        int x = 566, y = 528; 
  
        System.out.println("Before Swap"); 
        System.out.println("x = " + x); 
        System.out.println("y = " + y); 
  
        int t = x; 
        x = y; 
        y = t; 
  
        System.out.println("After swap"); 
        System.out.println("x = " + x); 
        System.out.println("y = " + y); 
    } 
}
