public class UnaryOperatorExeample1 
{
    public static void main(String[] args)
     {
         int a=10;
         int b=10;
         System.out.println(a++);
         System.out.println(a++ + a++);
         System.out.println(++a + ++a);
         System.out.println(a++ + ++a);
         System.out.println(++a + a++);
         System.out.println(a--);
         System.out.println(a-- + a--);
         System.out.println(--a + --a);
         System.out.println(a-- + --a);
         System.out.println(--a + a--);
         System.out.println(b++);
         System.out.println(b++ + b++);
         System.out.println(++b + ++b);
         System.out.println(b++ + ++b);
         System.out.println(++b + b++);
         System.out.println(b--);
         System.out.println(b-- - b--);
         System.out.println(--b - --b);
         System.out.println(b-- - --b);
         System.out.println(--b - b--);
        
    }
    
}
