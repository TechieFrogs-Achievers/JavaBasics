public class UnaryOperator 
{
   public static void main(String[] args) 
   {
   int x=14;
   System.out.println (x++); 
   System.out.println(++x);
   System.out.println(x--);
   System.out.println(--x);
   System.out.println("the x++ value is:"+ x++);
   System.out.println("the ++x value is :"+ ++x);
   System.out.println("the x-- value is:" + x--);
   System.out.println("the --x value is:" + --x);

   }
}
