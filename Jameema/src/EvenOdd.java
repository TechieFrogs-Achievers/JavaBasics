public class EvenOdd

 {
    public static void main(String[] args)
     {
         int n=69;
         if( n/2==0 ) //condition is true
         {
             System.out.println("the given num is even :" +n); // it prints when the condition is true

         }
        else //condition is false
        {
            System.out.println("the given num is odd :" +n); // it prints when condition is false
        } 
    }
}
