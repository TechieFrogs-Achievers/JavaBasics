public class AlphabetBoth
 {
     public static void main(String[] args) 
     {
        System.out.println("uppercase:");
        for(char i=65;i<=90;i++)
        {
            
            System.out.print( i+ "  ");

        }
        System.out.println(" \n lowercase:");
        
        for(char j=97;j<=122;j++)
        {
            System.out.print(j+"  ");
        }
       
     }
     
}
