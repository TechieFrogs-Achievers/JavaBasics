public class Alphabet 
{
    public static void main(String[] args) 
    {
        char j='(';
        if ( (j>='a' && j<='z') || (j>='A' && j<='Z'))
        {
            System.out.println("the character" +j+" is alphabet ");
        }
        else
        {
            System.out.println("the character "+j+" is not an alphabet  ");
        }
        
    }
    
}
