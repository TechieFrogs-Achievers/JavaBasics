public class SwitchExercise 
{
    public static void main(String[] args) {
        char color='p';

        switch(color)
        {
            case 'b':
            System.out.println("Colour is    Blue");
            break;
            case 's':
            System.out.println("Colour is    SkyBlue");
            break;
            case 'm':
            System.out.println("Colour is     Maroon");
            break;
            case 'p':
            System.out.println("Colour is    Pink");
            break;
            case 'w':
            System.out.println("Colour is    white");
            break;
            case 'y':
            System.out.println("Colour is    yellow");
            break;
            default:
            System.out.println("We can't define color for that character");


        }

    }
    
}
