public class Arithmetic {
    public static void main(String args[])
    {  
        int a=200;  
        int b=4;  
        System.out.println("addition is:" + (a+b));//204  
        System.out.println("subtraction is :" +( a-b));//196
        System.out.println("multiplication is:" +a*b);//800 
        System.out.println("division is:" + a/b);//50
        System.out.println("modulus is:" + a%b);//0 
    }

        
    
    
    
}
