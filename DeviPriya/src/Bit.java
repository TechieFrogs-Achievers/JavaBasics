public class Bit {
    public static void main(String args[]) {
int a=4;  
int b=6;  
int c=8;
System.out.println(a>b&a++<c);  //false && true = false  
System.out.println(a);          //6 because second condition is checked  
System.out.println(a>b|a++<c);  //false && true = false  
System.out.println(a);          //6 because second condition is checked  


        
    }
    
}
