public class DataType {
    public static void main(String args[]) {
        int sample_var1=200;
        float sample_var2= 10.2f;
        double sample_var3=10.22222222;
        char sample_var4='D';
        boolean sample_var5=true;
        short sample_var6=100;
        long sample_var7=900000L;
        byte sample_var8=100;
        System.out.println("int sample value:"+sample_var1);
        System.out.println("float sample value:"+sample_var2);
        System.out.println("double sample value:"+sample_var3);
        System.out.println("char sample value:"+sample_var4);
        System.out.println("boolean sample value:"+sample_var5);
        System.out.println("short sample value:"+sample_var6);
        System.out.println("long sample value:"+sample_var7);
        System.out.println("byte sample value:"+sample_var8);


        
    }
    
}
