public class EvenOdd { 
    static int Num=30;
    public static void main(String[] args) {
        
        if (Num % 2 == 0)
        {
            System.out.println(" Num is Even" +Num);
        }
        else
        {
            System.out.println(" Num is Odd" +Num);
        }
        
    }

    
}
