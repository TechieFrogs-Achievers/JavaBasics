public class Sum {
    public static void main(String[] args) 
    {
        int Num = 20;
        int Sum = 0;
        for(int i =1;i<=Num;i++)
        {
            Sum = Sum+i;
        }
            System.out.println("Sum =" +Sum);

            //using while loop 
            int i=10;
            while(i<=Num)
            {
                Sum = Sum+i;
                i++;
                System.out.print("sum =:" +Sum +" ");

            }
 

    }
    
}
