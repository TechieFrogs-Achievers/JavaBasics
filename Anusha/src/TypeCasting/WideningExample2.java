package TypeCasting;

public class WideningExample2 
{
    public static void main(String[] args) 
    {
        short i=12;
        int j=i;
        float k =j;
        
        System.out.println("short value: "+i);
        System.out.println("int value: "+j);
        System.out.println("float value: "+k);
    }
}
