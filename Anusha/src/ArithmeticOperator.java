public class ArithmeticOperator {
    public static void main(String[] args) {
        int x=30;
        int y=15;
        System.out.println("Addition of two numbers:"+(x+y));//45
        System.out.println("Subtraction of two numbers:"+(x-y));//15
        System.out.println("Multiplication of two numbers:"+(x*y));//450
        System.out.println("division of two numbers:"+(x/y));//2(gives the quotient as result)
        System.out.println("division of two numbers:"+(x%y));//0(gives the remainder as result)
    }
}
