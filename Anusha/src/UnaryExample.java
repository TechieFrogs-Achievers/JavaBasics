public class UnaryExample
 {
     //main method
    public static void main(String[] args)
    {
      int value1=-10;
      int value2=11;
      System.out.println(~value1);//9(positive of total positive value which starts from 0)
      System.out.println(~value2);//-12(false of total minus, positive starts from 0)
      boolean value=true;//false
      boolean val=false;
      System.out.println(!value);
      System.out.println(!val);

    }
}
