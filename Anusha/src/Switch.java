public class Switch
{
    //main method
  public static void main(String[] args)
  {
    char value='a';
    switch(value)
    {
        case 'x': 
                 System.out.println("the letter is :"+value);
                 break;
        case 'y': 
                 System.out.println("the letter is :"+value);
                 break;
        case 'z': 
                 System.out.println("the letter is :"+value);
                 break;  
        case 'a': 
                 System.out.println("the letter is :"+value);
                 break; 
        default: 
                 System.out.println("not found");
                 break;      
    }

  }  
}
