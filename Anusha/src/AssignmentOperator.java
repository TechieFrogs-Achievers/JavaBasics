public class AssignmentOperator
 {
     //main method
    public static void main(String[] args) 
    {
        int a=10;  
        int b=20;  
        a+=b;//a=a+b (a=10+20=30)
        b-=a;//b=b-a (b=20-30=10)  
        System.out.println("the value of a is:"+a);  
        System.out.println("the value of b is:"+b);  

    }
}
