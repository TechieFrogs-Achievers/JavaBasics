class Loop  
{ 
  
    public static void main(String args[]) 
    { 
        String array[] = { "jemmi", "bindu", "shirin" }; 
   
        for (String x : array)
         { 
            System.out.println(x); 
        } 
    
        for (int i = 0; i < array.length; i++)  
        {  
            System.out.println(array[i]);  
        }  
        
    } 
} 
