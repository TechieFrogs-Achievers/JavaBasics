public class Swapping 
{
     
        public static void main(String[] args) 
        {  
           int x, y, t;  
          
           System.out.println("Enter the value of X and Y");  
           x = 5;
           y = 7; 
           System.out.println("before swapping numbers: "+x +"  "+ y);  
             
           t = x;  
           x = y;  
           y = t;  
           System.out.println("After swapping: "+x +"   " + y);  
           System.out.println( );  
        }    
     
    
}
