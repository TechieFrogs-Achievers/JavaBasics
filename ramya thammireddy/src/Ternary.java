public class Ternary {
    

        public static void main(String args[]){
        
        int a=20;
        
        int b=30;
        
        int min=(a<b)?a:b;
        int max=(a>b)?a:b;
        
        System.out.println(min); //b=30
        System.out.println(max); //a=20
        
        
    }  
}
