public class Numbers 
{
      
        public static void main(String[] args)
        {  
       int lines=10;  
        int i=1;  
        int j;  
        for(i=1;i<=lines;i++)
        
        {  
           for(j=1;j<=i;j++)
           {  
               System.out.print(i*j+" ");  
           }  
            System.out.println("");  
       }  
     }  
    
    
}
