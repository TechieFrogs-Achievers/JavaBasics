public class ForLoop
{
    public static void main(String[] args) 
    {
     

     //printing values


      for(int i=0;i<10;i++)
      {
          System.out.println(i);
      }
      
      

      
      
      //finding sum
     
     
      int sum = 0;
      int n = 5;

    for (int i = 1; i <= n; ++i)
     {
      sum += i;  // sum = sum + i
                  
    }
       
    System.out.println("Sum = " + sum); 



    }
    
}
