public class EvenOdd
 {
     public static void main(String[] args) 
     {
       
       int number = 0;

       if(number!=0)
       {
           if(number%2==0)
           {
            System.out.println("Given number is EVEN number");
           }
           else
           {
            System.out.println("Given number is ODD number");
        
           }
        }
        else
        {
             System.out.println("zero");
        }
    }
}
