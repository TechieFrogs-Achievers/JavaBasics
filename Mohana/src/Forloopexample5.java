public class Forloopexample5 
{
    public static void main(String[] args) { 
        
        // for loop 
        
        for( int i = 1 ; i <= 5 ; i++ ) 
        {
            for( int j = i ; j <= 5 ; j++ )
            {
                System.out.print( " * ");
                //System.out.print( i );
            }
            System.out.println();
        }
    }
    
}
