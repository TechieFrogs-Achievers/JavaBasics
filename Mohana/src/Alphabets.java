public class Alphabets  
{
    public static void main(String[] args) 
    {
        
        // lowercase alphabets 

        for(char  ch = 97 ; ch <= 122 ; ch++ ) 
        {
            System.out.print(  ch +  "  "   );
        }   

        System.out.println(  " \n" + "---------- Capital letters ----------"); 
        
        //uppercase alphabets 

       for( char CH = 65 ; CH <= 90 ; CH++ ) 
       {
            System.out.print(  CH + "  ");
       }
        
    }
    
}
 