public class JavaexDiv2 
{

    public static void main(String[] args) {
        

        int  x = 25  ,  y = 5 ; 

        // division operation 
         
        int z = x / y ; // It gives Quotient  as 5
        int k = x % y ; // It gives Remainder as 0 

        System.out.println( " The quotient is = " + z );
        System.out.println( " The remainder is = " + k );

    }
    
}
