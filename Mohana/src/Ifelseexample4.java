public class Ifelseexample4 
{
    public static void main(String[] args) {
        
        int number = 20; 

        // ifelse 

        if((number % 2 == 0) && (number % 5 == 0))
        {
            System.out.println( " The number is divisible by both 2 & 5");
        }
        else
        {
            System.out.println( " Not divisible ");
        }
    }
    
}
