public class ShiftOperators 
{
    public static void main(String[] args)
     {
     //Shift operators 
     
     int a = 20;
     int b = 3;
       
     //leftshift

     System.out.println( a << b ); //160
     System.out.println( a << 2 ); //80

     // Right shift 

     System.out.println( a >> b ); //2
     System.out.println( a >> 2 ); //5
    } 
    
}
