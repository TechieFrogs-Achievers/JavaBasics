public class TernararyOperator
{ 
    public static void main(String[] args)
    {

    int  pen = 10;
    int pencil = 5 ;

    // Terinary operator 
      
    int big = ( pen > pencil ) ?   pen : pencil ;
    System.out.println( big ); 

    float small = ( pen < pencil ) ? pen : pencil;
    System.out.println( small);
        
    }
    
}
