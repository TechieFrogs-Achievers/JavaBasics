public class Switch {
    public static void main(String[] args) {
 // Switch Statement for Numbers       
        int n=30;
        switch(n){
            case 10:
            System.out.println("Number is 10");
            break;
            case 20:
            System.out.println("Number is 20");
            break;
            case 30:
            System.out.println("Number is 30");
            break;
            default:
            System.out.println("Invalid Number");

        }

 // Switch Statement for Vowels
        char ch='i';
        switch(ch){
            case 'a':
            System.out.println("Vowel");
            case 'e':
            System.out.println("Vowel");
            case 'i':
            System.out.println("Vowel");
            case 'o':
            System.out.println("Vowel");
            case 'u':
            System.out.println("Vowel");
            case 'A':
            System.out.println("Vowel");
            case 'I':
            System.out.println("Vowel");
            case 'E':
            System.out.println("Vowel");
            case 'O':
            System.out.println("Vowel");
            case 'U':
            System.out.println("Vowel");
            default:
            System.out.println("Consonant");
         

        }

    
    }
}
