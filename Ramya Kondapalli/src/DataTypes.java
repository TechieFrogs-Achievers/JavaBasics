public class DataTypes {
    public static void main(String args[])
    {

        boolean booleanType = true;
        System.out.println("Boolean : " + booleanType);

        byte byteType = 10;
        System.out.println("Byte Datatype :" + byteType);

        short shortType = -5000;
        System.out.println("Short datatype :" + shortType);

        int intType = 564;
        System.out.println("Int datatype :" + intType);

        long longType = -200000L;
        System.out.println("Long Datatype :" + longType);

        char charType = 's';
        System.out.println("char dataType :" + charType);

        float floatType = 0.26f;
        System.out.println("float datatype :" + floatType);

        double doubleType = 2537380.666d;
        System.out.println("Double Datatype :" + doubleType);

    }
}
