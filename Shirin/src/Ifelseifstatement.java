public class Ifelseifstatement 
{
    public static void main(String[] args) 
    {
        int numvalue = 10;
        
        if (numvalue <= 10) //numvalue =10 
        {
            System.out.println("the numvalue is positive");
        }
        else if(numvalue > 10) // numvalue >=10(negatie)
        {
             
            System.out.println("the numvalue is negative");

        }
        else 
        {
            
            System.out.println(" the numvalue is null");
        }
        
    }
}
