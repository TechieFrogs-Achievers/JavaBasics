public class Alphabetscases 
{
    public static void main(String[] args)
    {
        for (char a = 65 ; a <= 90 ; a++)
        {
            System.out.println("uppercase : "  + a );
            
        }
        for ( char b = 97; b <= 122 ; b++ )
        {
            System.out.println("lowercase  : " + b);
            
        }
        
    }
    
}
