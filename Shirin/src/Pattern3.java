public class Pattern3
{
    public static void main(String[] args) 
    { 
        // Half pyramid using alphabets
        char i;
        char j;
        for (  i = 'A' ; i <= 'E'; i++)
        {
            for ( j = 'A' ; j <= i ; j++)
            {
                System.out.print(i);
            }
            System.out.println();
            
        }
        
        
    }
    
}
