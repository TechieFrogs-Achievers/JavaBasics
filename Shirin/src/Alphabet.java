public class Alphabet
{
    public static void main(String[] args) 
    {
        char t = 'f'; // '*', '@'

        if (( t >= 'a' && t <= 'z') || ( t >= 'A' &&  t <= 'Z' ) )
        {
            System.out.println(" t is an alphabet " + t);
        }
        else 
        {
            System.out.println(" t is not alphabet " + t);

        }
        
    }
    
}
